/**
Procedura usuwujaca tabele, jeśli istnieją
*/

DECLARE
   V_COUNT INT;
   V_NAME  VARCHAR2(20);
   TYPE NAMESARRAY IS
      VARRAY(13) OF VARCHAR2(20);
   NAMES   NAMESARRAY;
BEGIN
   NAMES := NAMESARRAY(
      'users',
      'refresh_tokens',
      'preferences',
      'currencies',
      'methods_of_payment',
      'groups',
      'archived_groups',
      'roles',
      'memberships',
      'temporary_memberships',
      'archived_memberships',
      'expenses',
      'categories'
   );

   FOR I IN 1..NAMES.COUNT LOOP
      V_NAME := NAMES(I);
      SELECT COUNT(*)
        INTO V_COUNT
        FROM USER_TABLES
       WHERE TABLE_NAME = UPPER(V_NAME);
      IF V_COUNT = 1 THEN
         DBMS_OUTPUT.PUT_LINE('Dropping table: ' || V_NAME);
         EXECUTE IMMEDIATE 'DROP TABLE '
                           || V_NAME
                           || ' CASCADE CONSTRAINTS';
      END IF;
   END LOOP;
END;
/

CREATE SEQUENCE BASE_GROUP_SEQ START WITH 1 INCREMENT BY 1;

CREATE SEQUENCE BASE_MEMBERSHIP_SEQ START WITH 1 INCREMENT BY 1;

CREATE TABLE USERS (
   ID             NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   CREATION_DATE  DATE NOT NULL,
   PREFERENCES_ID NUMBER(6,0) NOT NULL UNIQUE,
   EMAIL          VARCHAR2(255 CHAR) NOT NULL UNIQUE,
   NAME           VARCHAR2(255 CHAR) NOT NULL,
   PASSWORD       VARCHAR2(255 CHAR) NOT NULL,
   SURNAME        VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE REFRESH_TOKENS (
   ID          NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   USER_ID     NUMBER(6,0) NOT NULL UNIQUE,
   EXPIRY_DATE TIMESTAMP(6) WITH TIME ZONE NOT NULL,
   TOKEN       VARCHAR2(255 CHAR) NOT NULL UNIQUE,
   PRIMARY KEY ( ID )
);

CREATE TABLE PREFERENCES (
   ID          NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   CURRENCY_ID NUMBER(6,0) NOT NULL,
   METHOD_ID   NUMBER(6,0) NOT NULL,
   LANGUAGE    VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE CURRENCIES (
   ID            NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   EXCHANGE_RATE FLOAT(53) NOT NULL,
   NAME          VARCHAR2(255 CHAR) NOT NULL,
   SYMBOL        VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID ),
   UNIQUE ( NAME )
);

CREATE TABLE METHODS_OF_PAYMENT (
   ID   NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   NAME VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID ),
   UNIQUE ( NAME )
);

CREATE TABLE GROUPS (
   ID   NUMBER(6,0) NOT NULL,
   NAME VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE ARCHIVED_GROUPS (
   ID   NUMBER(6,0) NOT NULL,
   NAME VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE ROLES (
   ID   NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   NAME VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID ),
   UNIQUE ( NAME )
);

CREATE TABLE MEMBERSHIPS (
   ID       NUMBER(6,0) NOT NULL,
   GROUP_ID NUMBER(6,0) NOT NULL,
   ROLE_ID  NUMBER(6,0) NOT NULL,
   USER_ID  NUMBER(6,0) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE TEMPORARY_MEMBERSHIPS (
   ID        NUMBER(6,0) NOT NULL,
   GROUP_ID  NUMBER(6,0) NOT NULL,
   ROLE_ID   NUMBER(6,0) NOT NULL,
   SENDER_ID NUMBER(6,0) NOT NULL,
   USER_ID   NUMBER(6,0) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE ARCHIVED_MEMBERSHIPS (
   ID       NUMBER(6,0) NOT NULL,
   GROUP_ID NUMBER(6,0) NOT NULL,
   ROLE_ID  NUMBER(6,0) NOT NULL,
   USER_ID  NUMBER(6,0) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE EXPENSES (
   ID            NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   EXPENSE_DATE  DATE NOT NULL,
   PRICE         FLOAT(53) NOT NULL,
   CATEGORY_ID   NUMBER(6,0) NOT NULL,
   CURRENCY_ID   NUMBER(6,0) NOT NULL,
   MEMBERSHIP_ID NUMBER(6,0) NOT NULL,
   METHOD_ID     NUMBER(6,0) NOT NULL,
   TITLE         VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID )
);

CREATE TABLE CATEGORIES (
   ID   NUMBER(6,0)
      GENERATED BY DEFAULT AS IDENTITY,
   NAME VARCHAR2(255 CHAR) NOT NULL,
   PRIMARY KEY ( ID ),
   UNIQUE ( NAME )
);